#include <iostream>

using namespace std;

int main() {
    int d = 17;
    int m = 8;
    int y = 1988;
    cout << "Birthday dec:\t" << d << "\t" << m << "\t" << y << endl;
    cout << "Birthday oct:\t" << oct << d << "\t" << m << "\t" << y << endl;
    cout << "Birthday hex:\t" << hex << d << "\t" << m << "\t" << y << endl;
    return 0;
}
